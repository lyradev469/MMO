// Starting Stats per Job
// ----------------------------------------------------------

export function getStartingStats(jobId: string) {
  const defaults = { str: 1, int: 1, dex: 1, vit: 1, agi: 1, luk: 1 };
  switch (jobId) {
    case "swordsman":
    case "knight":
    case "lord_knight":
      return { ...defaults, str: 8, vit: 6, agi: 5 };
    case "mage":
    case "wizard":
    case "high_wizard":
      return { ...defaults, int: 9, dex: 6, agi: 4 };
    case "archer":
    case "hunter":
    case "sniper":
      return { ...defaults, dex: 9, agi: 7, luk: 5 };
    default:
      return defaults;
  }
}

export function calculateDerived(stats: { str: number; int: number; dex: number; vit: number; agi: number; luk: number }, level: number, baseHp: number, baseSp: number) {
  return {
    maxHp: baseHp + stats.vit * 10 + level * 20,
    hp: baseHp + stats.vit * 10 + level * 20,
    maxSp: baseSp + stats.int * 5 + level * 8,
    sp: baseSp + stats.int * 5 + level * 8,
    atk: stats.str * 4 + stats.dex * 1 + level * 2,
    matk: stats.int * 5 + level * 2,
    def: stats.vit * 3 + level * 1,
    mdef: stats.int * 2 + stats.vit * 1,
    flee: stats.agi * 3 + level * 1,
    hit: stats.dex * 4 + level * 2,
    aspd: Math.min(190, 100 + stats.agi * 2),
    critRate: Math.floor(stats.luk / 3) + 1,
    critDmg: 150,
  };
}
