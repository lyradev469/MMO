// ----------------------------------------------------------
// EXP Table (base levels 1-99)
// ----------------------------------------------------------

function generateExpTable(): LevelData[] {
  const table: LevelData[] = [];
  for (let lvl = 1; lvl <= 99; lvl++) {
    // Ragnarok-inspired exponential curve
    const expToNext = Math.floor(10 * Math.pow(lvl, 2.3) + 50 * lvl);
    const expRequired = lvl === 1 ? 0 : table[lvl - 2].expRequired + table[lvl - 2].expToNext;
    table.push({ level: lvl, expRequired, expToNext });
  }
  return table;
}

export const EXP_TABLE = generateExpTable();

